def add(x, y):
    return x + y
def subtract(x, y):
    return x - y
def multiply(x, y):
    return x * y
def divide(x, y):
    return x // y

x = int(input("Enter the first number:"))
y = int(input("Enter the second number:"))

# Find the addition
print("Addition:", add(x, y))

# Find the difference
print("subtraction:", subtract(x, y))

# Find the  product
print("Multiplication:", multiply(x, y))

# Find the Quotient
print("Division:", divide(x, y))



